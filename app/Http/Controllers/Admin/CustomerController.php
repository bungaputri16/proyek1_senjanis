     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'nama' => 'required',
            'email' => 'required',
            'hp' => 'required',
            'alamat' => 'required',
        ]);
    
        Customer::create([
            'nama' =>$request->nama,
            'email' =>$request->email,
            'hp' =>$request->hp,
            'alamat' =>$request->alamat,

        ]);

        return redirect()->route('customer.index');
    }

